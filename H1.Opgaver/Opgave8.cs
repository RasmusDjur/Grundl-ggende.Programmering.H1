{
    public static void Kør()
    {
        Console.Clear();
        // Brug en for-løkke til at udskrive tallene fra 1 til 10
        for (int i = 1; i <= 10; i++)  // Start ved 1, fortsæt så længe i <= 10, og øg i med 1 for hver iteration
        {
            Console.WriteLine(i);  // Udskriv tallet
        }

